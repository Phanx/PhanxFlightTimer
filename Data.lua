local _, defaults = ...
PhanxFlightDataDefaults = defaults

local function AddFlightData(t, source, suffix)
	for faction, factionData in pairs(source) do
		for startPoint, endPointData in pairs(factionData) do
			for endPoint, flightTime in pairs(endPointData) do
				tinsert(t, strjoin(" | ", faction, startPoint, endPoint, flightTime, suffix))
			end
		end
	end
end
function MergeAllFlightData()
	local t = {}
	AddFlightData(t, PhanxFlightDataDefaults, "DEFAULT")
	AddFlightData(t, InFlightVars, "INFLIGHT")
	AddFlightData(t, PhanxFlightData, "SAVED")
	sort(t)
	return t
end
